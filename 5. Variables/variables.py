# Creating Variables
x = 5
y = "John"
print(x)
print(y)

# No need declaration of type
b = 4       # b is of type int
b = "Sally" # b is now of type str
print(b)

# Casting
p = str(3)    # p will be '3'
q = int(3)    # q will be 3
r = float(3)  # r will be 3.0
print(f"p = {p} q = {q} r = {r}\n")

#Get the Type
a = 5
c = "John"
print(type(a))
print(type(c))